#include <iostream>
using namespace std;

int main() {
    float n;
    float p = 3.14;
    cin>> n;
    cout << "The diameter of circle is equal to " << 2 * n << endl;
    cout << "The circumference of circle is equal to " << 2 * p * n << endl;
    cout << "The area of circle is equal to " << p * n * n << endl;
    return 0;
    }